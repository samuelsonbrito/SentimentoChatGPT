    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        OpenAiService service = new OpenAiService(API_KEY);

        CompletionRequest request = CompletionRequest.builder()
                .model("text-davinci-003")
                .prompt("Decida se o sentimento do comentário é positivo, neutro ou negativo. Comentário: "+txtComentario.getText()+" Sentimento:")
                .maxTokens(100)
                .build();

        String status = service.createCompletion(request).getChoices().get(0).getText().trim();
        
        lbStatus.setText(status);
        
        if(status.equalsIgnoreCase("positivo")){
            lbStatus.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/positivo.png")));
        }else if(status.equalsIgnoreCase("negativo")){
            lbStatus.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/negativo.png")));
        }else if(status.equalsIgnoreCase("neutro")){
            lbStatus.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/neutro.png")));
        }

    }//GEN-LAST:event_jButton1ActionPerformed

